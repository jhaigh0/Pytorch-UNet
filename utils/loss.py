ComboLoss_ALPHA = 0.5 # < 0.5 penalises FP more, > 0.5 penalises FN more
ComboLoss_CE_RATIO = 0.5 #weighted contribution of modified CE loss compared to Dice loss

class ComboLoss(nn.Module):
    def __init__(self, weight=None, size_average=True):
        super(ComboLoss, self).__init__()

    def forward(self, inputs, targets, smooth=1, alpha=ComboLoss_ALPHA, eps=1e-9):
        
        assert inputs.size() == targets.size()

        #flatten label and prediction tensors
        inputs = inputs.reshape(-1)
        targets = targets.reshape(-1)
        
        #True Positives, False Positives & False Negatives
        intersection = torch.dot(inputs, targets)    
        dice = (2. * intersection + smooth) / (inputs.sum() + targets.sum() + smooth)
        
        inputs = torch.clamp(inputs, eps, 1.0 - eps)       
        out = - (ComboLoss_ALPHA * ((targets * torch.log(inputs)) + ((1 - ComboLoss_ALPHA) * (1.0 - targets) * torch.log(1.0 - inputs))))
        weighted_ce = out.mean(-1)
        combo = (ComboLoss_CE_RATIO * weighted_ce) - ((1 - ComboLoss_CE_RATIO) * dice)
        
        return combo

